# 1. Тоо тааварлах (if/elif, user input, type casting)
# Хэрэглэгчээс 1-100 хооронд тоо оруулахыг хүс. Тухайн тоо:

# 50-аас бага бол "Бага байна"
# 50-тай тэнцүү бол "Зөв тааварлалаа!"
# 50-аас их бол "Их байна" гэж хэвлэ.


user=int(input("Give Me Any Number You Want:"))
if user<50:
    print("Бага Байна!")
elif user==50:
    print("Зөв Тааварлалаа!")
else:
    print("Их байна !")
