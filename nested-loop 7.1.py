# 6. Хоосон зайтай тэгш өнцөгт
# n=4, m=4 хэмжээтэй, гэхдээ зөвхөн хүрээгээр нь *, 
# дотор нь хоосон зай байх тэгш өнцөгт хэвлэ:

for i in range(4):
    for b in range(4):
        if i==0 or i==3 or b==0 or b==3:
            print("*", end="")
        else:
            print(" ", end="")
    print()
